async function fetchBuffer(url) {
  const response = await fetch(url)
  if (!response.ok) throw new Error(`${response.status} ${url}`)
  return Buffer.from(await response.arrayBuffer())
}

async function renderSilhouette(country) {
  const url = `https://raw.githubusercontent.com/djaiss/mapsicon/master/all/${country.iso2}/vector.svg`
  let source
  let usedFallback = false
  try {
    source = (await fetchBuffer(url)).toString('utf8')
      .replace(/fill="#000000"/g, 'fill="#173757"')
      .replace(/stroke="none"/g, 'stroke="#b57a26"')
  } catch (error) {
    usedFallback = true
    const geoFile = path.join(worldCountriesRoot, 'data', `${country.id.toLowerCase()}.geo.json`)
    const countryFeature = JSON.parse(await fs.readFile(geoFile, 'utf8'))
    const countryProjection = geoMercator().fitExtent([[26, 22], [486, 362]], countryFeature)
    const silhouettePath = geoPath(countryProjection).digits(1)(countryFeature)
    let markerCircles = ''
    if (ARCHIPELAGO_MARKERS.has(country.id)) {
      const separatedPoints = []
      const geometries = countryFeature.type === 'FeatureCollection'
        ? countryFeature.features.map((item) => item.geometry)
        : [countryFeature.geometry]
      const polygons = geometries.flatMap((geometry) => (
        geometry?.type === 'MultiPolygon' ? geometry.coordinates : []
      ))
      for (const coordinates of polygons) {
        const point = countryProjection(geoCentroid({ type: 'Polygon', coordinates }))
        if (point && separatedPoints.every(([x, y]) => Math.hypot(point[0] - x, point[1] - y) > 34)) {
          separatedPoints.push(point)
        }
      }
      markerCircles = separatedPoints
        .map(([x, y]) => `<circle cx="${x.toFixed(1)}" cy="${y.toFixed(1)}" r="18"/>`)
        .join('')
    }
    source = `<svg xmlns="http://www.w3.org/2000/svg" width="512" height="384" viewBox="0 0 512 384"><g fill="#173757" stroke="#b57a26" stroke-width="8"><path d="${silhouettePath}" stroke-linejoin="round"/>${markerCircles}</g></svg>`
    console.warn(`\nGeoJSON fallback: ${country.id} (${error.message})`)
  }
  let silhouetteImage = sharp(Buffer.from(source))
    .resize(460, 348, { fit: 'inside', withoutEnlargement: false })
  if (usedFallback) silhouetteImage = silhouetteImage.dilate(7)
  const trimmed = await silhouetteImage.png().toBuffer()
  await sharp({
    create: { width: 512, height: 384, channels: 4, background: { r: 0, g: 0, b: 0, alpha: 0 } },
  })
    .composite([{ input: trimmed, gravity: 'centre' }])
    .png({ compressionLevel: 9, palette: true })
    .toFile(path.join(outputDirectory, `${country.id}.png`))
}

const queue = [...library]
